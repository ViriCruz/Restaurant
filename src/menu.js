  let img = document.createElement('img');
  img.src = maki;
  img.alt = 'Maki sushi';
  let li = listItem('dish', 'maki-sushi', 'Maki sushi');
  li.childNodes[0].classList.add('dish-description');
  li.appendChild(img);
  ul.appendChild(li);
  li = listItem('dish', 'norimaki-sushi', 'Norimaki sushi');
  li.childNodes[0].classList.add('dish-description');
