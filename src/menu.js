  img.src = oshizushi;
  img.alt = 'Oshizushi sushi';
  li.appendChild(img);
  ul.appendChild(li);
  li = listItem('dish', 'uramaki-sushi', 'Uramaki sushi');
  li.childNodes[0].classList.add('dish-description');
